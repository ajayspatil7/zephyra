class ZephyraTokens:
    # Padding token
    PAD = "<pad>"
    # Beginning of sentence and End of sentence
    BOS = "<s>"
    EOS = "</s>"
    # Junk
    UNK = "<unk>"
    # Masking token
    MASK = "<mask>"
    # Chat format tokens
    USER = "<|user|>"
    ASSISTANT = "<|assistant|>"
    SYSTEM = "<|system|>"
